package Array;

public class Sample1 //Multidimentional array
{
public static void main(String[] args) 
{
	int[][]ar= {{10,20,30},{40,50,60},{70,80,90}};
	for(int i=0; i<=2; i++)
	{
		for(int j=0; j<=2; j++) 
		
		{
			
			System.out.print(ar[i][j]+" ");	
		
		}
System.out.println();

	}
}
}